export const getMovieById = (id: string): Movie | undefined => {
  return movies.find((m) => m.id === id);
};

export const getMoviesByGenre = (genreId: string): Movie[] => {
  return movies.filter((m) => m.genres.some((g) => g.id === genreId));
};

export const searchMovies = (query: string): Movie[] => {
  const lowerQuery = query.toLowerCase();
  return movies.filter(
    (m) =>
      m.title.toLowerCase().includes(lowerQuery) ||
      m.director.name.toLowerCase().includes(lowerQuery) ||
      m.actors.some((a) => a.name.toLowerCase().includes(lowerQuery))
  );
};

export const getTrendingMovies = (): Movie[] => {
  return [...movies].sort((a, b) => b.rating - a.rating).slice(0, 10);
};

export const getRecommendedMovies = (): Movie[] => {
  return [...movies].sort(() => Math.random() - 0.5).slice(0, 10);
};
